    private void tabel_showMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_showMouseClicked
        // TODO add your handling code here:
        // Lấy vị trí hàng, lấy được ID
        javax.swing.JTable source;
        source = (javax.swing.JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        String id = (String) source.getModel().getValueAt(row, 0);
        // Đổ data vào form
        NhanVien a = this.danhsachNhanVien.my_dict.get(id);
        this.fillData2Fields(a);
    }//GEN-LAST:event_tabel_showMouseClicked

    private void btt_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_editActionPerformed
        // TODO add your handling code here:
        if (danhsachNhanVien.my_dict.containsKey(this.line_ID.getText())) {
            try {
                if (danhsachNhanVien.my_dict.containsKey(this.line_ID.getText()) == true) {
                    SimpleDateFormat fm = new SimpleDateFormat("dd/MM/yyyy");
                    String ID = this.line_ID.getText();
                    String name = this.line_name.getText();
                    boolean sex = this.radio_sex_nam.isSelected();
                    String country = this.line_country.getText();
                    Date birthday = fm.parse(this.line_birthday.getText());
                    Date intoday = fm.parse(this.line_intoDate.getText());
                    if (this.radio_cauthu.isSelected()) {
                        String vitri = this.line_vitri.getText();
                        int soLuotTran = Integer.parseInt(this.line_soluottran.getText());
                        int soBanThang = Integer.parseInt(this.line_banthang.getText());
                        double luongThoaThuan = Double.parseDouble(this.line_luongthoathuan.getText());
                        CauThu a = new CauThu(ID, name, vitri, sex, birthday, intoday, vitri, soLuotTran, soBanThang, luongThoaThuan);
                        this.danhsachNhanVien.suaNhanVien(this.line_ID.getText(), a);
                        this.loadTable();
                    } else {
                        int soNamKinhNghiem = Integer.parseInt(this.line_kinhnghiem.getText());
                        double luongCoBan = Double.parseDouble(this.line_luongcoban.getText());
                        double phuCap = Double.parseDouble(this.line_phucap.getText());
                        double heSoLuong = Double.parseDouble(this.line_hesoluong.getText());
                        HuanLuyenVien a = new HuanLuyenVien(ID, name, country, sex, birthday, intoday, soNamKinhNghiem, luongCoBan, phuCap, heSoLuong);
                        this.danhsachNhanVien.suaNhanVien(this.line_ID.getText(), a);
                        this.loadTable();
                    }
                } else {
                    // khi khác id, hỏi người dùng có muốn add hay không
                    int result = javax.swing.JOptionPane.showConfirmDialog((java.awt.Component) null, "Bạn vừa chỉnh sửa ID, bạn có muốn thêm Nhân Viên này không?",
                            "alert", javax.swing.JOptionPane.OK_CANCEL_OPTION);
                    if (result == 0) {
                        this.addNhanVien();
                    }
                }

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Có gì đó sai sai");
            }
        }
        
    }//GEN-LAST:event_btt_editActionPerformed

    private void btt_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_delActionPerformed
        // TODO add your handling code here:
        if (this.line_ID.getText().equals("")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Bạn chưa nhập ID\nCần ID để xóa!");
        }
        else if (this.danhsachNhanVien.my_dict.containsKey(this.line_ID.getText()) == false) {
            javax.swing.JOptionPane.showMessageDialog(this, "ID không tồn tại");
        }
        else{
            String ID = this.line_ID.getText();
            this.danhsachNhanVien.xoaNhanVien(ID);
            this.loadTable();
            this.cleanBasicFields();
            this.cleanCauThuFields();
            this.cleanHuanLuyenVienFields();
            javax.swing.JOptionPane.showMessageDialog(this, "Đã xóa nhân viên có ID: "+ID);
        }
    }//GEN-LAST:event_btt_delActionPerformed

    private void btt_findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_findActionPerformed
        // TODO add your handling code here:
        String result = (String)JOptionPane.showInputDialog(
               this,
               "Nhập tên nhân viên", 
               "Tìm kiếm nhân viên",            
               JOptionPane.PLAIN_MESSAGE,
               null,            
               null, 
               ""
            );
        System.out.println(result);
        for(NhanVien a: this.danhsachNhanVien.my_dict.values()){
            if (a.getTenNhanVien().equals(result)) {
                this.cleanBasicFields();
                this.cleanCauThuFields();
                this.cleanHuanLuyenVienFields();
                this.fillData2Fields(a);
                return;
            }
        }
        if (result != null) {
            JOptionPane.showMessageDialog(this, "Không tin thấy nhân viên với tên: "+result);
        }
    }//GEN-LAST:event_btt_findActionPerformed

    private void btt_luongCaoNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_luongCaoNhatActionPerformed
        // TODO add your handling code here:
        String id = this.danhsachNhanVien.luongCaoNhat();
        if (id == null) {
            JOptionPane.showMessageDialog(this, "Chưa có nhân viên nào hết :D");
            return;
        }
        NhanVien a = this.danhsachNhanVien.my_dict.get(id);
        if (a instanceof CauThu){
            CauThu b = (CauThu) a;
            String msg = "Nhân viên có mức lương cao nhất là "+b.getTenNhanVien()+"\n"
                    + "Với mức lương: "+String.valueOf(b.tinhLuong());
            JOptionPane.showMessageDialog(this, msg);
        }
        else {
            HuanLuyenVien b = (HuanLuyenVien) a;
            String msg = "Nhân viên có mức lương cao nhất là "+b.getTenNhanVien()+"\n"
                    + "Với mức lương: "+String.valueOf(b.tinhLuong());
            JOptionPane.showMessageDialog(this, msg);
        }
    }//GEN-LAST:event_btt_luongCaoNhatActionPerformed

    private void fillData2Fields(NhanVien a) {
        this.line_ID.setText(a.getID());
        this.line_name.setText(a.getTenNhanVien());
        this.line_country.setText(a.getQuocTich());
        if (a.isGioiTinh()) {
            this.radio_sex_nam.setSelected(true);
        } else {
            this.radio_sex_nu.setSelected(true);
        }
        this.line_birthday.setText(a.getNgaySinh());
        this.line_intoDate.setText(a.getNgayVaolam());
        if (a instanceof CauThu) {
            CauThu b = (CauThu) a;
            this.setGroupCauThuEnable();
            this.setGroupHuanLuyenVienDisable();
            this.line_vitri.setText(b.getViTriThiDauString());
            this.line_luongthoathuan.setText((String.valueOf(b.getLuongThoaThuan())));
            this.line_soluottran.setText(String.valueOf(b.getSoLuotTranThamGia()));
            this.line_banthang.setText(String.valueOf(b.getSoBanThang()));
        } else {
            HuanLuyenVien b = (HuanLuyenVien) a;
            this.setGroupHuanLuyenVienEnable();
            this.setGroupCauThuDisable();
            this.line_kinhnghiem.setText(String.valueOf(b.getSoNamKinhNghiem()));
            this.line_luongcoban.setText(String.valueOf(b.getLuongCoBan()));
            this.line_phucap.setText(String.valueOf(b.getPhuCap()));
            this.line_hesoluong.setText(String.valueOf(b.getHeSoLuong()));
        }
    }
    
