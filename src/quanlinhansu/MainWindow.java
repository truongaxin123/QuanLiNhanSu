    private void addNhanVien() {
        try {
            if (danhsachNhanVien.my_dict.containsKey(this.line_ID.getText()) == false) {
                SimpleDateFormat fm = new SimpleDateFormat("dd/MM/yyyy");
                String ID = this.line_ID.getText();
                String name = this.line_name.getText();
                boolean sex = this.radio_sex_nam.isSelected();
                String country = this.line_country.getText();
                Date birthday = fm.parse(this.line_birthday.getText());
                Date intoday = fm.parse(this.line_intoDate.getText());
                if (this.radio_cauthu.isSelected()) {
                    String vitri = this.line_vitri.getText();
                    int soLuotTran = Integer.parseInt(this.line_soluottran.getText());
                    int soBanThang = Integer.parseInt(this.line_banthang.getText());
                    double luongThoaThuan = Double.parseDouble(this.line_luongthoathuan.getText());
                    CauThu a = new CauThu(ID, name, country, sex, birthday, intoday, vitri, soLuotTran, soBanThang, luongThoaThuan);
                    danhsachNhanVien.themNhanVien(ID, a);
                    this.loadTable();
//                    this.addRow(a);
                } else {
                    int soNamKinhNghiem = Integer.parseInt(this.line_kinhnghiem.getText());
                    double luongCoBan = Double.parseDouble(this.line_luongcoban.getText());
                    double phuCap = Double.parseDouble(this.line_phucap.getText());
                    double heSoLuong = Double.parseDouble(this.line_hesoluong.getText());
                    HuanLuyenVien a = new HuanLuyenVien(ID, name, country, sex, birthday, intoday, soNamKinhNghiem, luongCoBan, phuCap, heSoLuong);
                    danhsachNhanVien.themNhanVien(ID, a);
                    this.loadTable();
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "ID bị trùng, xin nhập lại ID");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Có gì đó sai sai");
        }
    }

    private void loadTable() {
        System.out.println("load table");
        Vector Col = new Vector();
        Col.addElement("ID");
        Col.addElement("Tên");
        Col.addElement("Quốc tịch");
        Col.addElement("Giới tính");
        Col.addElement("Ngày Sinh");
        Col.addElement("Ngày Vào");
        Vector data = new Vector();
        for (String k : this.danhsachNhanVien.my_dict.keySet()) {
            NhanVien a = this.danhsachNhanVien.my_dict.get(k);
            Vector nhanvien = new Vector();
            nhanvien.addElement(a.getID());
            nhanvien.addElement(a.getTenNhanVien());
            nhanvien.addElement(a.getQuocTich());
            nhanvien.addElement(a.getGioiTinh());
            nhanvien.addElement(a.getNgaySinh());
            nhanvien.addElement(a.getNgayVaolam());
            data.addElement(nhanvien);
        }
        this.tabel_show.setModel(new DefaultTableModel(data, Col));
    }

